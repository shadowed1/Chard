#!/bin/bash
# chard_scale — Display scaling configuration for Chard

RED=$'\033[31m'
GREEN=$'\033[32m'
YELLOW=$'\033[33m'
BLUE=$'\033[34m'
MAGENTA=$'\033[35m'
CYAN=$'\033[36m'
BOLD=$'\033[1m'
RESET=$'\033[0m'

CHARD_SCALE_FILE="/.chard_scale"
BASE_DPI=160
DAEMON_INTERVAL=120


update_env_file() {
    local var="$1" value="$2" env_file="$HOME/.bashrc"
    sed -i "/^export $var=/d" "$env_file"
    echo "export $var=$value" >> "$env_file"
}

write_scale_file() {
    local scale_float="$1" gdk_scale="$2"
    local qt_dpi xcursor_size xcursor_theme
    qt_dpi=$(awk "BEGIN { printf \"%d\", int(96 * $scale_float) }")
    xcursor_size="${XCURSOR_SIZE:-32}"
    xcursor_theme="${XCURSOR_THEME:-Adwaita}"

    if [[ ! -f "$CHARD_SCALE_FILE" ]]; then
        sudo tee "$CHARD_SCALE_FILE" >/dev/null <<< ""
        sudo chown 1000:1000 "$CHARD_SCALE_FILE"
    fi
    tee "$CHARD_SCALE_FILE" >/dev/null <<EOF
# Generated by chard_scale
CHARD_SCALE=$scale_float
GDK_SCALE=1
GDK_DPI_SCALE=$scale_float
QT_SCALE_FACTOR=$scale_float
QT_AUTO_SCREEN_SCALE_FACTOR=1
QT_WAYLAND_FORCE_DPI=$qt_dpi
ELECTRON_FORCE_DEVICE_SCALE_FACTOR=$scale_float
_JAVA_OPTIONS=-Dsun.java2d.uiScale=$scale_float
XCURSOR_SIZE=$xcursor_size
XCURSOR_THEME=$xcursor_theme
EOF
}

apply_scale() {
    local scale_float="$1"

    local gdk_scale qt_dpi
    gdk_scale=$(awk "BEGIN { v = int($scale_float); if (v < 1) v = 1; if ($scale_float >= 1.5) v = 2; print v }")
    qt_dpi=$(awk "BEGIN { printf \"%d\", int(8 * $scale_float) }")

    export CHARD_SCALE="$scale_float"
    export GDK_SCALE="$gdk_scale"
    export GDK_DPI_SCALE=1
    export QT_SCALE_FACTOR="$scale_float"
    export QT_AUTO_SCREEN_SCALE_FACTOR=1
    export QT_WAYLAND_FORCE_DPI="$qt_dpi"
    export ELECTRON_FORCE_DEVICE_SCALE_FACTOR="$scale_float"
    export _JAVA_OPTIONS="-Dsun.java2d.uiScale=$scale_float"
    export XCURSOR_THEME="${XCURSOR_THEME:-Adwaita}"
    local xcursor_size
    xcursor_size=$(awk "BEGIN { s=int(8*$scale_float); if(s<32)s=32; if(s>64)s=64; print s }")
   export XCURSOR_SIZE=$xcursor_size
    update_env_file "XCURSOR_SIZE" "$xcursor_size"
    update_env_file "XCURSOR_THEME" "${XCURSOR_THEME}"
    update_env_file "CHARD_SCALE"                        "$scale_float"
    update_env_file "GDK_SCALE"                          "1"
    update_env_file "GDK_DPI_SCALE"                      "$scale_float"
    update_env_file "QT_SCALE_FACTOR"                    "$scale_float"
    update_env_file "QT_AUTO_SCREEN_SCALE_FACTOR"        "1"
    update_env_file "QT_WAYLAND_FORCE_DPI"               "$qt_dpi"
    update_env_file "ELECTRON_FORCE_DEVICE_SCALE_FACTOR" "$scale_float"
    update_env_file "_JAVA_OPTIONS"                      "-Dsun.java2d.uiScale=$scale_float"

    if ! write_scale_file "$scale_float" "$gdk_scale"; then
        echo "${YELLOW}Warning: could not write $CHARD_SCALE_FILE${RESET}" >&2
    fi

    echo "${GREEN}Scaling applied: CHARD_SCALE=${scale_float}  GDK_SCALE=${gdk_scale}  QT_WAYLAND_FORCE_DPI=${qt_dpi}${RESET}" >&2
}

detect_scale_from_modetest() {
    local raw
    raw=$(modetest -c 2>/dev/null | awk '
    BEGIN { RS="" }
    {
        connector=""; size=""; resolution=""; refresh=""
    }
    /connected/ {
        for (i=1; i<=NF; i++) {
            if ($i == "connected") {
                connector=$(i+1)
                size=$(i+2)
            }
        }
    }
    /#0/ {
        for (i=1; i<=NF; i++) {
            if ($i ~ /^#0$/) {
                resolution=$(i+1)
                refresh=$(i+2)
            }
        }
    }
    {
        if (connector && resolution) {
            print connector ": " resolution " @ " refresh "Hz (" size " mm)"
        }
    }
    ')

    [[ -z "$raw" ]] && return 1

    local best_ppi=0 best_scale="" display_count=0

    while IFS= read -r line; do
        if [[ "$line" =~ ^([^:]+):\ +([0-9]+)x([0-9]+)\ +@\ +[0-9.]+Hz\ +\(([0-9]+)x([0-9]+)\ +mm\) ]]; then
            local name="${BASH_REMATCH[1]}"
            local w_px="${BASH_REMATCH[2]}"  h_px="${BASH_REMATCH[3]}"
            local w_mm="${BASH_REMATCH[4]}"  h_mm="${BASH_REMATCH[5]}"
            (( display_count++ ))

            local ppi
            ppi=$(awk "BEGIN {
                diag_px = sqrt($w_px^2 + $h_px^2)
                diag_in = sqrt($w_mm^2 + $h_mm^2) / 25.4
                printf \"%.1f\", diag_px / diag_in
            }")

            echo "  $name → ${w_px}x${h_px} (${w_mm}x${h_mm} mm) @ ${ppi} PPI" >&2

            if awk "BEGIN { exit !($ppi+0 > $best_ppi+0) }"; then
                best_ppi="$ppi"
                best_scale=$(awk -v base_dpi="$BASE_DPI" "BEGIN {
	        s = ($ppi / base_dpi)
	        if (s < 1.0)  s = 1.0
	        if (s > 2.25) s = 2.25
	        printf \"%.2f\", s
	    }")
            fi
        fi
    done <<< "$raw"

    [[ -z "$best_scale" ]] && return 1
    echo "$best_scale"
}


run_daemon() {
    echo "${CYAN}${BOLD}chard_scale daemon started (interval: ${DAEMON_INTERVAL}s)${RESET}" >&2
    local last_scale=""

    while true; do
        local new_scale
        new_scale=$(detect_scale_from_modetest 2>/dev/null)

        if [[ -z "$new_scale" ]]; then
            sleep "$DAEMON_INTERVAL"
            continue
        fi

        if [[ "$new_scale" != "$last_scale" ]]; then
            echo "${YELLOW}[chard_scale] Scale change: ${last_scale:-<unset>} -> ${new_scale}${RESET}" >&2
            apply_scale "$new_scale"
            last_scale="$new_scale"
        fi

        sleep "$DAEMON_INTERVAL"
    done
}

set_display_scaling() {
    local input="$1"
    if [[ "$input" =~ ^[0-9]+([.][0-9]+)?$ ]]; then
        local scale
        scale=$(awk "BEGIN {
            s = $input + 0
            if (s < 0.25) s = 1.0
            if (s > 4.0)  s = 4.0
            printf \"%.2f\", s
        }")
        apply_scale "$scale"
    else
        echo "${RED}Error: scale must be a number between 0.25 and 4.0${RESET}" >&2
        return 1
    fi
}

set_cursor_scaling() {
    local input="$1"
    if ! [[ "$input" =~ ^[0-9]+$ ]]; then
        echo "${RED}Error: cursor size must be an integer.${RESET}" >&2
        return 1
    fi
    local size=$input
    (( size < 8s   )) && size=8
    (( size > 200 )) && size=200
    export XCURSOR_SIZE=$size
    update_env_file "XCURSOR_SIZE" "$size"
    echo "${GREEN}Cursor size set to ${size}${RESET}" >&2
}

case "$1" in
    display)
        if [[ -z "$2" ]]; then
            echo; echo "${GREEN}Current display scaling: ${CHARD_SCALE}${RESET}"; echo
        else
            set_display_scaling "$2"
        fi
        ;;
    cursor)
        if [[ -z "$2" ]]; then
            echo; echo "${GREEN}Current cursor size: ${XCURSOR_SIZE}${RESET}"; echo
        else
            set_cursor_scaling "$2"
        fi
        ;;
    daemon)
        run_daemon
        ;;
    *)
        echo "Usage: chard_scale { display [scale] | cursor [size] | detect | daemon }" >&2
        ;;
esac
